public class Calculadora {

    public static int suma (int arg1 , int arg2){
        return arg1 + arg2;
    }
    public static int resta (int arg1 , int arg2){
        return arg1 - arg2;
    }
    public static int producto (int arg1 , int arg2){
        return arg1 * arg2;
    }
    public static double division (double arg1 , double arg2){
        return arg1 / arg2;
    }
}
